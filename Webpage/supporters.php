<p>	
The Aegean Dendrochronology Project which began in 1973 in the basement of the Ankara Museum, and then the Dendro Lab at Cornell (still in a basement), were both founded and created by Peter Ian Kuniholm (Professor and Director Emeritus). The Cornell Laboratory subsequently became <a href="http://www.malcolmwiener.net">The Malcolm and Carolyn Wiener</a> Laboratory for Aegean and Near Eastern Dendrochronology.</p>

<p>After three decades at the helm, Peter retired in 2006. Sturt Manning became the new Director of the Laboratory from July 2006, and is also Goldwin Smith Professor of Classical Archaeology in the Department of Classics at Cornell.</p>

<p>Today the Cornell Lab holds some 40,000 dendro samples which it has measured and studied, and actively continues work building and studying long robust tree-ring chronologies for the Mediterranean and Near East from the present day back as far as possible.</p>

