<p>
Our key long-range goal is to build long multi-millennial scale tree-ring chronologies in the Aegean and Near East that will extend from the present to the early Holocene to cover, broadly speaking, the last 10,000 years of human and environmental history. Our raison d'&ecirc;tre is to provide a dating method for the study of history and prehistory in the Aegean that is accurate to the year. This kind of precision has, up to now, been lacking in ancient studies of this area. Indeed, few archaeological problems stimulate as much rancor as chronology, especially that of the Eastern Mediterranean. The work of the Aegean and Near Eastern Dendrochronology Project aims to help to bring some kind of rational and neutral order to Aegean and Near Eastern chronology from the Neolithic to the present.
</p>

<p>
We also aim to provide a fundamental climate and environmental studies resource for a region which was the cradle of a number of civilizations central to human history from the origins of agriculture through to the Classical period, the Medieval period, and beyond. 
</p>

<p>To date, more than 10 million tree-ring measurements have led to the compilation of chronologies covering (but not wholly covering) some 9,000 years. Our aim is to fill in the gaps. See our <a href="http://dendro.cornell.edu/photo.php?photourl=/images/charts/bargraph.jpg">bargraph</a> to get an idea of the time periods covered. The most difficult periods for us are the first half millennium B.C. and the first millennium A.D. If you have relevant samples, <a href="/samples.php">please contact us</a>!
</p>

