<p>	
Corina is an open source, freely available, dendrochronology program used and developed by the Cornell Tree-Ring Laboratory.  
</p>

<p>
The main features of Corina include:
</p>

<ul>
<li>Support for standard serial and USB measuring platforms.</li>
<li>Multiple cross dating measures including T-scores, Weiserjahre and Trend.</li> 
<li>Standard dendro file format support including Tucson, Heidelberg and TSAP.</li>
<li>Advanced graphing support.</li>
</ul>

<p>
Corina is written in Java and so can be run on most operating systems include Windows, Mac OSX and Linux.  
</p>

