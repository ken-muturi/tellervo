<p>	
Charlotte joined the lab as a Research Associate in July 2007 from the University of Reading, UK, where she was manager of <a href="http://www.afess.co.uk">Archaeological, Forensic, and Environmental Scientific Services</a> within the School of Human and Environmental Sciences.</p>

<p>She has a background in interdisciplinary archaeological science research, with an degree in Environment and Archaeology, a masters in Geoarchaeology and a PhD in Archaeological Geochemistry.</p>

<p>She has been involved with excavations in the Severn Estuary and Scottish Hebrides (UK), and on Cyprus, and has lectured on a range of geoarchaeological subjects.</p>

<p>She has a particular interest in the application of Synchrotron Radiation X-ray Flourescence (SR-XRF), Laser Ablation Inductively Coupled Plasma Mass Spectrometry (LA-ICP-MS), solution ICP-MS, Inductively Coupled Plasma Atomic Emission Spectrometry (ICP-AES) and Scanning Electron Microscopy (SEM) to tree-rings and other paleoenvironmental archives. 
</p>

