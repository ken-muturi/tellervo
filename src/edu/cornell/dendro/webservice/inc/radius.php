    function writeToDB()
    {
        // Write the current object to the database

        global $dbconn;
        
        // Check for required parameters
        if($this->label == NULL) 
        {
            $this->setErrorMessage("902", "Missing parameter - 'label' field is required.");
            return FALSE;
        }
        if($this->specimenID == NULL) 
